public class Bean_Cadastro {


    private String sNome;
    private String sTel;
    private String sEnd;
    private String sSabor;
    private String sDataEnt;
    private String sTipoPizza;
    private String sTipoEntrega;
    private String sPago;

    public Bean_Cadastro() {
    }

    public String getsNome() {
        return sNome;
    }

    public void setsNome(String sNome) {
        this.sNome = sNome;
    }

    public String getsTel() {
        return sTel;
    }

    public void setsTel(String sTel) {
        this.sTel = sTel;
    }

    public String getsEnd() {
        return sEnd;
    }

    public void setsEnd(String sEnd) {
        this.sEnd = sEnd;
    }

    public String getsSabor() {
        return sSabor;
    }

    public void setsSabor(String sSabor) {
        this.sSabor = sSabor;
    }

    public String getsDataEnt() {
        return sDataEnt;
    }

    public void setsDataEnt(String sDataEnt) {
        this.sDataEnt = sDataEnt;
    }

    public String getsTipoPizza() {
        return sTipoPizza;
    }

    public void setsTipoPizza(String sTipoPizza) {
        this.sTipoPizza = sTipoPizza;
    }

    public String getsTipoEntrega() {
        return sTipoEntrega;
    }

    public void setsTipoEntrega(String sTipoEntrega) {
        this.sTipoEntrega = sTipoEntrega;
    }

    public String getsPago() {
        return sPago;
    }

    public void setsPago(String sPago) {
        this.sPago = sPago;
    }
}
